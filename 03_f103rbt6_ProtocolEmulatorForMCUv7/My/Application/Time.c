void TIME_Calculation(Time_t *time, uint32_t mScount){

	time->mSec = mScount;
	mScount   /= 1000;
	time->hour = (uint8_t)((mScount / 3600) % 24);
	time->min  = (uint8_t)((mScount / 60) % 60);
	time->sec  = (uint8_t)( mScount % 60);
}
